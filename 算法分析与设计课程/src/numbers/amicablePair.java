package numbers;

//思考题3：相亲数
public class amicablePair {
	public static void main(String[] args) {
		int i, j, k;
		final int N = 30000;
		int[] s = new int[N];
 
		for (i = 2; i < N; i++) {
			for (j = 1, s[i] = 0; j < i; j++)
				if (i % j == 0)
					s[i] += j;
		}
		for (i = 2; i < N; i++)
			for (j = i + 1; j < N; j++)
				if ((i == s[j]) && (j == s[i])) {
					System.out.print(i + ":");
					for (k = 1; k < i; k++)
						if (i % k == 0)
							System.out.print(k + " ");
					System.out.println();
					System.out.print(j + ":");
					for (k = 1; k < j; k++)
						if (j % k == 0)
							System.out.print(k + " ");
					System.out.println("\n");
				}

	}
}
